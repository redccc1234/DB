CREATE TABLE CUSTOMERS(
	CUSTOMER_ID  NUMBER PRIMARY KEY,
	NAME VARCHAR2(20) NOT NULL,
	GENDER CHAR(3) CONSTRAINT GENDER_CHECK CHECK(GENDER IN('남','여')),
	ADDRESS VARCHAR2(100),
	PHONE VARCHAR2(30)
